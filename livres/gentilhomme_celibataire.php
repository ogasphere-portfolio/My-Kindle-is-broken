<h1>Le gentilhomme célibataire </h1>
<p>Le mariage de lord Saint-Simon et son curieux dénouement
ont depuis longtemps cessé d’intéresser les cercles aristocratiques
au milieu desquels évolue celui qui fut le héros de cette
malheureuse aventure. De récents scandales agrémentés de plus
piquants détails en ont fait oublier le souvenir, et nul ne
s’intéresse plus aujourd’hui à ce drame vieux de quatre ans.
Néanmoins, comme j’ai tout lieu de croire que le gros public n’en
a jamais eu qu’un aperçu très sommaire et comme, d’autre part,
mon ami Sherlock Holmes contribua largement, en l’occurrence,
à trouver le mot de l’énigme, j’estime que ces mémoires seraient
incomplets si je n’y faisais point figurer le récit de ce singulier
épisode. </p>