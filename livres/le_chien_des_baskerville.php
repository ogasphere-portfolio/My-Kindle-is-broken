<h1>CHAPITRE I</h1>
<h2>MONSIEUR SHERLOCK HOLMES</h2>

<p>M. SHERLOCK HOLMES se levait habituellement fort
tard, sauf lorsqu’il ne dormait pas de la nuit, ce qui lui arrivait
parfois. Ce matin là, pendant qu’il était assis devant son petit
déjeuner, je ramassais la canne que notre visiteur avait oubliée,
la veille au soir. C’était un beau morceau de bois, solide, terminé
en pommeau. Juste au-dessous de ce pommeau, une bague
d’argent qui n’avait pas moins de deux centimètres de haut portait
cette inscription datant de 1884 : « À James Mortimer,
M.R.C.S.1, ses amis du C.C.H. ». Une belle canne ; canne idéale
pour un médecin à l’ancienne mode : digne, rassurante…</p>
<p>« Eh bien, Watson, que vous suggère cette canne ? »</p>
<p>Holmes me tournait le dos, et je n’avais rien fait qui pût le
renseigner sur mon occupation du moment.</p>