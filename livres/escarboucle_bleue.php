<h1>L’escarboucle bleue</h1>
<p>C’était le surlendemain de Noël. Je m’étais rendu chez mon
ami Sherlock Holmes, afin de lui présenter les vœux d’usage en
cette période de l’année. Je le trouvai en robe de chambre
pourpre, allongé sur son divan, son râtelier à pipes à portée de la
main. Sur le parquet, un tas de journaux, dépliés et froissés,
indiquait qu’il avait dépouillé avec soin la presse du matin. On
avait approché du divan une chaise, au dossier de laquelle était
accroché un chapeau melon, graisseux et minable, bosselé par
endroits et qui n’était plus neuf depuis bien longtemps. Une loupe
et une pince, posées sur le siège, donnaient à penser que le triste
objet n’avait été placé là qu’aux fins d’examen.</p>
<p>– Vous êtes occupé, dis-je. Je vous dérange ? </p>